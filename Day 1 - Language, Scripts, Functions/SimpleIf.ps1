$x = 4

if ($x -eq 1) { 
    Write-Host "We're number 1!" 
}
elseif ($x -eq 2) { 
    Write-Host "Number 2 tries harder." 
}
elseif ($x -eq 3) { 
    Write-Host "The trinity is a divine mystery." 
}
elseif ($x -eq 4) { 
    Write-Host "Or a character in the Matrix?" 
}
else { 
    Write-Host "Maybe next year :( " 
}  
